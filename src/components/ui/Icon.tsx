interface IconProps {
  name: string;
  className?: string;
  size?: number;
}

export function DynamicIcon({ name, className = "", size = 24 }: IconProps) {
  const IconComponent = iconMap[name];
  if (!IconComponent) return null;
  return <IconComponent size={size} className={className} />;
}
